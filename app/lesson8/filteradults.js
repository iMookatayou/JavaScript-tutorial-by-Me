const age = [12, 18, 25, 30];

const adults = age.filter((age) => {
  return age >= 18;
});

console.log(adults); // [18, 25, 30]

// สร้างตัวแปร age เก็บอาเรย์อายุของคน 4 คน
// ใช้เมธอด filter เพื่อกรองเอาเฉพาะคนที่มีอายุ 18 ปีขึ้นไป แล้วเก็บผลลัพธ์ไว้ในตัวแปร adults
// แสดงผลลัพธ์ของ adults ออกทางหน้าจอคอนโซล
// ผลลัพธ์ที่ได้
// [18, 25, 30]